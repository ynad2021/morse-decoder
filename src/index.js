const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};


function decode(expr) {
    let arr = expr.match(/.{1,10}/g);
    
    let morseArr = arr.map(x => x.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-').replace(/\*{10}/g, ' '));

    MORSE_TABLE[' '] = ' ';

    let decodedArr = morseArr.map(item => MORSE_TABLE[item]);  
   
    return decodedArr.join('');
};

module.exports = {
    decode
}
